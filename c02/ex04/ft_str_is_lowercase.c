int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	b;

	i = 0;
	while (str[i])
	{
		b = str[i] - '0';
		if (b <= 48 || b >= 75)
			return (0);
		i++;
	}
	return (1);
}
