#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	aux;

	i = 0;
	while (size > i)
	{
		if (tab[i] > tab[i + 1])
		{
			aux = tab[i];
			tab[i] = tab[i + 1];
			tab[i] = aux;
			i = 0;
		}
		else
		{
			i++;
		}
	}
}
